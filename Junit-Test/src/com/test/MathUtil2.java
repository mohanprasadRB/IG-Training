package com.test;

public class MathUtil2 {

	int add(int a, int b)
	{
		return a+b;
	}
	
	public int sub(int a, int b)
	{
		return a-b;
	}
	
	public int mul(int a, int b)
	{
		return a*b;
	}
	
	public int div(int a, int b)
	{
		return a/b;
	}
	
	double circleArea(double radius)
	{
		return Math.PI * radius * radius;
	}
}
